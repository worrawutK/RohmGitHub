using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Runtime.Serialization;

namespace Rohm.Common.CellController
{
    [DataContract()]
    public class MaterialInfo
    {

        private int c_Id;
        [DataMember()]
        public int Id
        {
            get { return c_Id; }
            set { c_Id = value; }
        }

        private string c_Name;
        [DataMember()]
        public string Name
        {
            get { return c_Name; }
            set { c_Name = value; }
        }

        private System.DateTime? c_SetupDate;
        [DataMember()]
        public System.DateTime? SetupDate
        {
            get { return c_SetupDate; }
            set { c_SetupDate = value; }
        }

        private System.DateTime? c_UsableDate;
        [DataMember()]
        public System.DateTime? UsableDate
        {
            get { return c_UsableDate; }
            set { c_UsableDate = value; }
        }

        private System.DateTime? c_WarningDate;
        [DataMember()]
        public System.DateTime? WarningDate
        {
            get { return c_WarningDate; }
            set { c_WarningDate = value; }
        }

        private System.DateTime? c_ExpiredDate;
        [DataMember()]
        public System.DateTime? ExpiredDate
        {
            get { return c_ExpiredDate; }
            set { c_ExpiredDate = value; }
        }

    }
}
